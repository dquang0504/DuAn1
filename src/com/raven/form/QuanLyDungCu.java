    private int row = -1;

    private void init() {
        this.fillTable();
        this.row = -1;
        this.updateStatus();
        lblUser.setText("User: " + Auth.user.getHoten());
        lblChucVu.setText("Chức vụ: " + (Auth.user.isVaiTro() ? "Quản lý" : "Huấn luyện viên"));
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblDungCu.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtSearch.getText();
            List<DungCu> list = dao.selectByKeyword(keyword); //đọc dữ liệu từ CSDL
            for (DungCu dc : list) {
                Object[] row = {dc.getMadc(), dc.getTendc(), dc.getGia(), dc.getSl(),
                    dc.getHinh()
                };
                model.addRow(row); //thêm từng hàng vào JTable
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
            System.out.println(e);
        }
    }

    DungCu getForm() {
        DungCu dc = new DungCu();
        dc.setMadc(txtMaDC.getText());
        dc.setTendc(txtTenDC.getText());
        dc.setGia(Double.parseDouble(txtGia.getText()));
        dc.setSl(Integer.parseInt(txtSoLuong.getText()));
        dc.setHinh(lblAnh.getToolTipText());
        return dc;
    }

    void setForm(DungCu dc) {
        txtTenDC.setText(dc.getTendc());
        txtMaDC.setText(dc.getMadc());
        txtGia.setText(String.valueOf(dc.getGia()));
        txtSoLuong.setText(String.valueOf(dc.getSl()));
        if (dc.getHinh() != null) {
            lblAnh.setToolTipText(dc.getHinh());
            lblAnh.setIcon(XImage.read(dc.getHinh()));
        }
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblDungCu.getRowCount() - 1);

        //Trạng thái form
        txtTenDC.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        //Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit);
        btnNext.setEnabled(edit);
        btnLast.setEnabled(edit && !last);
    }

    void clearForm() {
        DungCu dc = new DungCu();
        this.setForm(dc);
        this.row = -1;
        updateStatus();
    }

    void edit() {
        String makh = (String) tblDungCu.getValueAt(this.row, 0);
        DungCu kh = dao.selectById(makh);
        this.setForm(kh);
        this.updateStatus();
    }

