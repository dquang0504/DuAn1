    private void init() {
        this.fillTable();
        this.row = -1;
    }

    KhachHangDAO dao = new KhachHangDAO();

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtSearch.getText();
            List<KhachHang> list = dao.selectByKeyword(keyword); //đọc dữ liệu từ CSDL
            for (KhachHang kh : list) {
                Object[] row = {kh.getMaKH(), kh.getTenKH(), kh.getSdt(), kh.getEmail(),
                    kh.isGioiTinh() ? "Nam" : "Nữ"};
                model.addRow(row); //thêm từng hàng vào JTable
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
            System.out.println(e);
        }
    }

    void timKiem() {
        this.fillTable();
        this.row = -1;
    }
    
    public void chon(){
        data = (String) tblKhachHang.getValueAt(tblKhachHang.getSelectedRow(), 0);
        this.dispose();
}
    
    public String getSelectedData(){
        return this.data;
    }

